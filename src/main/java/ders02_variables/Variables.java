package ders02_Variables;

public class Variables {
    public static void main(String[] args) {

        int sayi; // Declaration sadece 1 kere yapılır.

        sayi=7; // Assignment istendiği kadar yapılabilir.
        System.out.println(sayi);

        sayi=10;
        System.out.println(sayi);

        sayi=sayi+10;
        System.out.println(sayi);

        String gs="Galatasaray";
        System.out.println(gs);




    }
}
